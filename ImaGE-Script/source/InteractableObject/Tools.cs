using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Tools : Entity, IInventoryItem
{
    public string Name
    {
        get
        {
            return "Tools Painting";
        }
    }

    public Sprite _Image = null;

    public Sprite Image
    {
        get
        {
            return _Image;
        }
    }


    public void OnPickup()
    {
        Entity.SetActive(false);

    }

    public void OnUsed()
    {
        Destroy(Entity);
    }
}
